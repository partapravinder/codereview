package com.newgen.model;

public class CopyToken {

	private String token;

	public CopyToken() {
		super();
	}

	public CopyToken(String token) {
		super();
		this.token = token;
	}

	/**
	 * @return the token
	 */
	public String getToken() {
		return token;
	}

	/**
	 * @param token the token to set
	 */
	public void setToken(String token) {
		this.token = token;
	}
	
	
}
