package com.newgen.model;

public class ReportsDataRequest {

	String dataClassField;
	String month;
	String year;

	public String getDataClassField() {
		return dataClassField;
	}

	public void setDataClassField(String dataClassField) {
		this.dataClassField = dataClassField;
	}

	public String getMonth() {
		return month;
	}

	public void setMonth(String month) {
		this.month = month;
	}

	public String getYear() {
		return year;
	}

	public void setYear(String year) {
		this.year = year;
	}

}
